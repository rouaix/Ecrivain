CREATE TABLE IF NOT EXISTS synopsis (
    id               INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    project_id       INT UNSIGNED NOT NULL UNIQUE,
    genre            VARCHAR(100) NULL,
    subgenre         VARCHAR(100) NULL,
    audience         VARCHAR(100) NULL,
    tone             VARCHAR(100) NULL,
    themes           TEXT NULL,
    comps            TEXT NULL,
    status           VARCHAR(50) NOT NULL DEFAULT 'en_cours',
    structure_method VARCHAR(50) NOT NULL DEFAULT 'libre',
    logline          TEXT NULL,
    pitch            TEXT NULL,
    situation        TEXT NULL,
    trigger_evt      TEXT NULL,
    plot_point1      TEXT NULL,
    development      TEXT NULL,
    midpoint         TEXT NULL,
    crisis           TEXT NULL,
    climax           TEXT NULL,
    resolution       TEXT NULL,
    is_exported      TINYINT(1) NOT NULL DEFAULT 1,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
